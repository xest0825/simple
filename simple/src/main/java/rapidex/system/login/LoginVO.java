package rapidex.system.login;

public class LoginVO {
	
	private String mb_id;
	private String login_typ;
	private String login_id;
	private String login_pw;
	private String smpl_pw;
	private String devc_id;
	
	
	public String getMb_id() {
		return mb_id;
	}
	public void setMb_id(String mb_id) {
		this.mb_id = mb_id;
	}
	public String getLogin_typ() {
		return login_typ;
	}
	public void setLogin_typ(String login_typ) {
		this.login_typ = login_typ;
	}
	public String getLogin_id() {
		return login_id;
	}
	public void setLogin_id(String login_id) {
		this.login_id = login_id;
	}
	public String getLogin_pw() {
		return login_pw;
	}
	public void setLogin_pw(String login_pw) {
		this.login_pw = login_pw;
	}
	public String getSmpl_pw() {
		return smpl_pw;
	}
	public void setSmpl_pw(String smpl_pw) {
		this.smpl_pw = smpl_pw;
	}
	public String getDevc_id() {
		return devc_id;
	}
	public void setDevc_id(String devc_id) {
		this.devc_id = devc_id;
	}
	
}
